function normalize(text) {
  return String(text ?? '').replace(/\s+/gu, ' ').trim();
}

function readText(filePath) {
  return decoder.decode(fs.readFileSync(filePath)).replace(/^\uFEFF/u, '');
}

function readParagraphs(fileName) {
  const fullPath = path.join(sourceDir, fileName);
  if (!fs.existsSync(fullPath)) {
    throw new Error(`Missing source file: ${fileName}`);
  }
  return readText(fullPath)
    .replace(/\r/gu, '')
    .split(/\n\s*\n+/u)
    .map(normalize)
    .filter(Boolean);
}

const paragraphCache = new Map();

function getParagraph(fileName, paragraphNumber) {
  if (!paragraphCache.has(fileName)) {
    paragraphCache.set(fileName, readParagraphs(fileName));
  }
  const paragraph = paragraphCache.get(fileName)[paragraphNumber - 1];
  if (!paragraph) {
    throw new Error(`Missing paragraph ${fileName}#${paragraphNumber}`);
  }
  return paragraph;
}

function csvEscape(value) {
  const text = value == null ? '' : String(value);
  if (/[",\n\r]/u.test(text)) return `"${text.replaceAll('"', '""')}"`;
  return text;
}

function writeCsv(filePath, records) {
  const headers = [
    'id',
    'book',
    'round',
    'status',
    'category',
    'title',
    'description',
    'source_file',
    'source_paragraph',
    'source_path',
    'keywords',
  ];
  const lines = [
    headers.join(','),
    ...records.map((record) => headers.map((header) => (
      header === 'keywords'
        ? csvEscape(record.keywords.join(';'))
        : csvEscape(record[header])
    )).join(',')),
  ];
  fs.writeFileSync(filePath, `\uFEFF${lines.join('\n')}\n`, 'utf8');
}

function writeMarkdown(filePath, records) {
  const lines = [
    `# ${bookTitle} 思想索引（提取轮）`,
    '',
    `- 书名：${bookTitle}`,
    '- 轮次：提取轮',
    '- 状态：待校对',
    `- 条目数：${records.length}`,
    '- 说明：标题为检索浓缩，description 保持源文本原段落。',
    '',
  ];
  for (const record of records) {
    lines.push(`## ${record.id}. ${record.title}`);
    lines.push('');
    lines.push(`- 分类：${record.category}`);
    lines.push(`- 来源：${record.source_file}#${record.source_paragraph}`);
    lines.push(`- 关键词：${record.keywords.join('、')}`);
    lines.push('');
    lines.push(record.description);
    lines.push('');
  }
  fs.writeFileSync(filePath, `${lines.join('\n')}\n`, 'utf8');
}

function writeTxt(filePath, records) {
  const text = `${records.map((record) => [
    `${record.id}. ${record.title}`,
    `分类：${record.category}`,
    `来源：${record.source_file}#${record.source_paragraph}`,
    `关键词：${record.keywords.join('、')}`,
    record.description,
  ].join('\n')).join('\n\n')}\n`;
  fs.writeFileSync(filePath, text, 'utf8');
}

