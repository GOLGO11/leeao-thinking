const sourceFiles = fs
  .readdirSync(sourceDir)
  .filter((name) => name.endsWith('.txt'));
const paragraphCache = new Map();

function normalize(text) {
  return String(text ?? '').replace(/\s+/g, ' ').trim();
}

function resolveSourceFile(sourceKey) {
  const file = sourceFiles.find((name) => name.startsWith(`${sourceKey}.`));
  if (!file) {
    throw new Error(`Missing source file for ${sourceKey}`);
  }
  return file;
}

function paragraphsFor(fileName) {
  if (!paragraphCache.has(fileName)) {
    const raw = decoder.decode(fs.readFileSync(path.join(sourceDir, fileName)));
    paragraphCache.set(
      fileName,
      raw
        .replace(/\r/g, '')
        .split(/\n\s*\n+/)
        .map(normalize)
        .filter(Boolean),
    );
  }
  return paragraphCache.get(fileName);
}

function csvEscape(value) {
  const text = String(value ?? '');
  if (/[",\n\r]/.test(text)) {
    return `"${text.replaceAll('"', '""')}"`;
  }
  return text;
}

function writeCsv(filePath, records) {
  const headers = [
    'id',
    'book',
    'round',
    'status',
    'category',
    'title',
    'description',
    'source_file',
    'source_paragraph',
    'source_path',
    'keywords',
  ];

  const rows = [
    headers.join(','),
    ...records.map((record) => headers.map((header) => csvEscape(record[header])).join(',')),
  ];

  fs.writeFileSync(filePath, `\uFEFF${rows.join('\n')}\n`, 'utf8');
}

function categoryCounts(records) {
  return taxonomy
    .map((category) => ({
      category,
      count: records.filter((record) => record.category === category).length,
    }))
    .filter((item) => item.count > 0);
}

function buildRecords() {
  return entries.map(([category, title, sourceKey, paragraphNumber, keywords], index) => {
    const sourceFile = resolveSourceFile(sourceKey);
    const sourceParagraphs = paragraphsFor(sourceFile);
    const description = sourceParagraphs[paragraphNumber - 1];
    if (!description) {
      throw new Error(`Missing paragraph ${sourceKey} P${paragraphNumber}`);
    }

    return {
      id: `LAT028-${String(index + 1).padStart(3, '0')}`,
      book: book.title,
      round: book.round,
      status: book.status,
      category,
      title,
      description,
      source_file: sourceFile,
      source_paragraph: paragraphNumber,
      source_path: path.relative(rootDir, path.join(sourceDir, sourceFile)).replaceAll(path.sep, '/'),
      keywords,
    };
  });
}

function writeMarkdown(filePath, payload) {
  const { records } = payload;
  const lines = [
    `# 《${book.title}》思想索引：${book.round}`,
    '',
    `- 状态：${book.status}`,
    `- 条目数：${records.length}`,
    '- 分类策略：继续使用 7 个原子分类；标题压缩，description 保留源文本原文段落。',
    '- 取材说明：跳过纯笑话、诗作、人物轶事过密和非李敖判断味太重的引文段落。',
    '',
  ];

  for (const category of taxonomy) {
    const items = records.filter((record) => record.category === category);
    if (!items.length) continue;
    lines.push(`## ${category}`);
    lines.push('');

    for (const record of items) {
      lines.push(`### ${record.id} ${record.title}`);
      lines.push('');
      lines.push(`- 来源：${record.source_file} P${record.source_paragraph}`);
      lines.push(`- 关键词：${record.keywords}`);
      lines.push('');
      lines.push(record.description);
      lines.push('');
    }
  }

  fs.writeFileSync(filePath, `${lines.join('\n')}\n`, 'utf8');
}

function writeSummary(filePath, payload) {
  const counts = categoryCounts(payload.records);
  const lines = [
    `# 《${book.title}》${book.round}说明`,
    '',
    `- 条目数：${payload.records.length}`,
    `- 源目录：${book.sourceDir}`,
    '- 说明：本轮为提取轮，尚未进入校对；所有 `description` 均由源文本段落直接写入。',
    '',
    '## 分类统计',
    '',
    ...counts.map((item) => `- ${item.category}：${item.count}`),
    '',
    '## 输出文件',
    '',
    '- 思想索引-提取轮.csv',
    '- 思想索引-提取轮.json',
    '- 思想索引-提取轮.md',
  ];

  fs.writeFileSync(filePath, `${lines.join('\n')}\n`, 'utf8');
}

